my_list = ["January", "February", "March"]
print(f"First list: {my_list}")
print(f"Printing just one index: {my_list[0]}")

# to add
my_list.append("April")
print(f"Printing list after add: {my_list}")
print(f"Printing index 3: {my_list[3]}")

# to remove
my_list.remove("January")
print(f"Printing list after remove: {my_list}")